/* Fallback for components rendered outside provider (e.g. React Router error boundary) */
function fallbackT(key: string): string {
  const entry = runtimeDict[key] ?? dict[key];
  return entry ? entry.ru : key;
}

const LanguageContext = createContext<LanguageContextType>({
  lang: "ru",
  setLang: () => {},
  t: fallbackT,
});

export function useLang() {
  return useContext(LanguageContext);
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const stored = localStorage.getItem("samga_lang");
    return stored === "kz" ? "kz" : "ru";
  });

  const setLang = (nextLang: Lang) => {
    localStorage.setItem("samga_lang", nextLang);
    setLangState(nextLang);
  };

  // F-22 (s23+): keep `<html lang>` in sync with the user's chosen
  // interface language so screen readers + browser auto-translate
  // pick up the right pronunciation / fallbacks.
  useEffect(() => {
    if (typeof document === "undefined") return;
    document.documentElement.lang = lang;
  }, [lang]);

  function t(key: string): string {
    const entry = runtimeDict[key] ?? dict[key];
    if (!entry) return key;
    return entry[lang];
  }

  return (
    <LanguageContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LanguageContext.Provider>
  );
}
