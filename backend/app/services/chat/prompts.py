# Builder
# ---------------------------------------------------------------------------


def build_chat_system_prompt(
    language: str,
    *,
    user_context: str | None = None,
    model_name: str = "",
    is_premium: bool = False,
    active_quota_type: str | None = None,
) -> str:
    """Compose the full system prompt for the chat endpoint.

    Sections are small, single-language, and deliberately non-redundant. The
    function-calling schema (see tools_registry.TOOLS) remains authoritative
    for tool names — we do not re-enumerate tools here."""
    lang = "kz" if language == "kz" else "ru"
    blocks: list[str] = []

    if lang == "kz":
        blocks.append(_ROLE_KZ)
        blocks.append(_LANGUAGE_KZ)
        blocks.append(build_identity_block(lang, model_name=model_name, is_premium=is_premium))
        blocks.append(_TOOL_PROTOCOL_KZ)
        blocks.append(_USER_DATA_FIDELITY_KZ)
        blocks.append(_DATA_YEAR_KZ)
        blocks.append(_LIBRARY_PROTOCOL_KZ)
        blocks.append(_MCQ_COMMITMENT_KZ)
        blocks.append(_QUOTA_KZ)
        blocks.append(_FORMATTING_KZ)
        blocks.append(_MATH_RENDERING_KZ)
        blocks.append(_PROBABILITY_LEGEND_KZ)
    else:
        blocks.append(_ROLE_RU)
        blocks.append(_LANGUAGE_RU)
        blocks.append(build_identity_block(lang, model_name=model_name, is_premium=is_premium))
        blocks.append(_TOOL_PROTOCOL_RU)
        blocks.append(_USER_DATA_FIDELITY_RU)
        blocks.append(_DATA_YEAR_RU)
        blocks.append(_LIBRARY_PROTOCOL_RU)
        blocks.append(_MCQ_COMMITMENT_RU)
        blocks.append(_QUOTA_RU)
        blocks.append(_FORMATTING_RU)
        blocks.append(_MATH_RENDERING_RU)
        blocks.append(_PROBABILITY_LEGEND_RU)

    active_quota_block = _build_active_quota_block(lang, active_quota_type)
    if active_quota_block:
        blocks.append(active_quota_block)

    if user_context:
        header = "ПРОФИЛЬ ПОЛЬЗОВАТЕЛЯ:" if lang == "ru" else "ПАЙДАЛАНУШЫ ПРОФИЛІ:"
        blocks.append(f"{header}\n{user_context.strip()}")

    return "\n\n".join(blocks)


