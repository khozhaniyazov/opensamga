"""
Modular system-prompt builder for Samga.ai chat.

Design:
    Before this module, the chat endpoint composed its system prompt from three
    overlapping sources:
      1. `ai_orchestrator.get_system_prompt(language)` — a 150-line monolith
         mixing role, language rules, tool protocol, data-year rules, quota
         advice, formatting rules, and probability phrasing.
      2. `ai_orchestrator.get_platform_identity_prompt(...)` — product identity.
      3. A ~200-line inline f-string inside `chat.py::chat_endpoint` that
         repeated most of (1) and additionally listed every tool by hand,
         disagreeing with the function-calling schema sent to the model.

    The duplication produced three concrete bugs:
      * Tool names in the prose disagreed with the schema → "unknown function".
      * Hardcoded "(2024 год)" drifted as new data years landed.
      * The KZ branch contained a long Russian-only "ОБЯЗАТЕЛЬНОЕ ИСПОЛЬЗОВАНИЕ
        БИБЛИОТЕКИ" block that contradicted its own "respond in Kazakh only"
        rule and polluted KZ outputs.

    The new model: this file exposes small, composable blocks (`role`,
    `language`, `tool_protocol`, `data_year`, `library_protocol`, `quota`,
    `formatting`, `probability_legend`) and one builder:

        build_chat_system_prompt(language, *, user_context=None,
                                 model_name="", is_premium=False)

    The builder returns the full system string. Each block is short, in one
    language, and mentions tools and years by reference rather than by
    hand-rolled lists. The function-calling schema (`tools_registry.TOOLS`)
    remains the only authoritative source of tool names and arguments.
"""

