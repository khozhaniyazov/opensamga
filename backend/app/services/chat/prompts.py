def _build_active_quota_block(language: str, active_quota_type: str | None) -> str | None:
    normalized = str(active_quota_type or "").strip().upper()
    if normalized not in {"GENERAL", "RURAL"}:
        return None
    if language == "kz":
        return (
            "БЕЛСЕНДІ ЕСЕПТЕУ КВОТАСЫ: ауыл квотасы."
            if normalized == "RURAL"
            else "БЕЛСЕНДІ ЕСЕПТЕУ КВОТАСЫ: жалпы конкурс."
        )
    return (
        "АКТИВНАЯ КВОТА ДЛЯ РАСЧЁТОВ: сельская квота."
        if normalized == "RURAL"
        else "АКТИВНАЯ КВОТА ДЛЯ РАСЧЁТОВ: общий конкурс."
    )


