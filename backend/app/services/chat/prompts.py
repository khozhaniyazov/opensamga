# s26 phase 5 (2026-04-27): the dashboard chat ships react-markdown +
# remark-math + rehype-katex. Math without `$...$` fences renders as
# literal text — so when the model writes raw `\frac{a}{b}` the user
# sees `fracab` (escapes get eaten by the markdown layer). The cure is
# in the prompt: wrap *every* LaTeX command in `$…$` (inline) or
# `$$…$$` (block). Plain ASCII formulas (`F = m * a`, `x^2 - 4 = 0`)
# stay plain — KaTeX is only for symbol-heavy math (\frac, \sqrt,
# \sum, integrals, vectors, Greek letters used as tokens).
